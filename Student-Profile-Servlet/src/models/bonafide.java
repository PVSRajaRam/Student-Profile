package models;

public class bonafide {
	private String applieddate;
	private String purpose;
	private String status;
	private String approvedstaff;
	private String approvedtime;
	private String preparedstaff;
	private String preparedtime;
	private String action;
	public String getApplieddate() {
		return applieddate;
	}
	public void setApplieddate(String applieddate) {
		this.applieddate = applieddate;
	}
	public String getPurpose() {
		return purpose;
	}
	public void setPurpose(String purpose) {
		this.purpose = purpose;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getApprovedstaff() {
		return approvedstaff;
	}
	public void setApprovedstaff(String approvedstaff) {
		this.approvedstaff = approvedstaff;
	}
	public String getApprovedtime() {
		return approvedtime;
	}
	public void setApprovedtime(String approvedtime) {
		this.approvedtime = approvedtime;
	}
	public String getPreparedstaff() {
		return preparedstaff;
	}
	public void setPreparedstaff(String preparedstaff) {
		this.preparedstaff = preparedstaff;
	}
	public String getPreparedtime() {
		return preparedtime;
	}
	public void setPreparedtime(String preparedtime) {
		this.preparedtime = preparedtime;
	}
	public String getAction() {
		return action;
	}
	public void setAction(String action) {
		this.action = action;
	}
	
	

}
